# This is the first step to get started with selenium

# Check what is installed
# $ pip freeze

# Install Selenium
# $ pip install selenium

# Install html-testRunner
# $ pip install html-testRunner

# Install WebDriver Manager 
# https://pypi.org/project/webdriver-manager/
# $ pip install webdriver-manager

# Check if installation was done, use one of below
# $ pip freeze
# $ pip list

# See the summary of selenium & html-testRunner package
# $ pip show selenium
# $ pip show html-testRunner

# Check for broken dependencies
# $ pip check selenium
# $ pip check html-testRunner

# Check the documentations
# https://pypi.org/project/html-testRunner/
